public class Pixel {

	public final int X;
	public final int Y;
	public int RED;
	public int GREEN;
	public int BLUE;
	public int ALPHA;

	public Pixel(int x, int y, int red, int green, int blue, int alpha) {
		this.X = x;
		this.Y = y;
		this.RED = red;
		this.GREEN = green;
		this.BLUE = blue;
		this.ALPHA = alpha;
	}

}
